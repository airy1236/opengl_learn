void LoadVertexData() {

    GLuint vbo;
    glGenVertexArrays(1, &vao);
    glGenBuffers(1, &vbo);

    glBindVertexArray(vao);

    glBindBuffer(GL_ARRAY_BUFFER, vbo);
    glBufferData(GL_ARRAY_BUFFER, sizeof(vertices), &vertices, GL_STATIC_DRAW);

    glVertexAttribPointer(0, 3, GL_FLOAT, GL_FALSE, 5 * sizeof(float), (void*)0);
    glEnableVertexAttribArray(0);
    glVertexAttribPointer(1, 2, GL_FLOAT, GL_FALSE, 5 * sizeof(float), (void*)(3 * sizeof(float)));
    glEnableVertexAttribArray(1);

    glBindVertexArray(0);
}

void LoadShader() {
    shader = new Shader("assets/shaders/vertex.vert", "assets/shaders/fragment.frag");
}

void LoadTexture() {
    texture1 = new Texture("assets/textures/3.jpg", 0);
    texture2 = new Texture("assets/textures/1.jpg", 1);
}

void transform() {
    glm::mat4 view = glm::mat4(1.0f);
    glm::mat4 projection = glm::mat4(1.0f);
    projection = glm::perspective(glm::radians(45.0f), (float)width / (float)height, 0.1f, 100.0f);
    view = glm::translate(view, glm::vec3(0.0f, 0.0f, -3.0f));

    shader->setTransform(projection, "projection");
    shader->setTransform(view, "view");
}

void render() {

    glClear(GL_COLOR_BUFFER_BIT | GL_DEPTH_BUFFER_BIT);

    //texture1->bind();
    //texture2->bind();

    shader->begin();

    shader->setInt("texture1", 0);
    shader->setInt("texture2", 1);

    transform();

    glBindVertexArray(vao);

    for (unsigned int i = 0; i < 10; ++i) {
        glm::mat4 model = glm::mat4(1.0f);
        model = glm::translate(model, cubePositions[i]);
        float angle = 20.0f * i;
        model = glm::rotate(model, glm::radians(angle), glm::vec3(1.0f, 0.3f, 0.5f));

        shader->setTransform(model, "model");

        glDrawArrays(GL_TRIANGLES, 0, 36);
    }

    glBindVertexArray(0);

    shader->end();
}

int main() {

    app->init(800, 600);

    glViewport(0, 0, 800, 600);

    glClearColor(0.4f, 0.3f, 0.5f, 1.0f);

    LoadVertexData();
    LoadShader();
    LoadTexture();

    while (app->update()) {
        
        render();
    }

    app->destory();
    delete shader;
    delete texture1;
    delete texture2;

	return 0;
}