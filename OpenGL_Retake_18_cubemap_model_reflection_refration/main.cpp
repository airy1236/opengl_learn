GLuint cubeVBO, cubeVAO, skyboxVBO, skyboxVAO;

void LoadVertex() {
    glGenVertexArrays(1, &skyboxVAO);
    glGenBuffers(1, &skyboxVBO);
    glBindVertexArray(skyboxVAO);
    glBindBuffer(GL_ARRAY_BUFFER, skyboxVBO);
    glBufferData(GL_ARRAY_BUFFER, sizeof(skyboxVertices), skyboxVertices, GL_STATIC_DRAW);
    glVertexAttribPointer(0, 3, GL_FLOAT, GL_FALSE, 3 * sizeof(float), (void*)0);
    glEnableVertexAttribArray(0);
    glBindVertexArray(0);
}

std::vector<std::string> faces{
    "assets/skybox/right.jpg",
    "assets/skybox/left.jpg",
    "assets/skybox/top.jpg",
    "assets/skybox/bottom.jpg",
    "assets/skybox/front.jpg",
    "assets/skybox/back.jpg"
};

GLuint loadCubemap(std::vector<std::string> faces) {
    stbi_set_flip_vertically_on_load(false);
    GLuint texture;
    glGenTextures(1, &texture);
    glBindTexture(GL_TEXTURE_CUBE_MAP, texture);
    int width, height, channels;
    for (unsigned int i = 0; i < faces.size(); ++i) {
        unsigned char* data = stbi_load(faces[i].c_str(), &width, &height, &channels, 0);
        if (data) {
            glTexImage2D(GL_TEXTURE_CUBE_MAP_POSITIVE_X + i, 0, GL_RGB, width, height, 0, GL_RGB, GL_UNSIGNED_BYTE, data);
            stbi_image_free(data);
        }
        else {
            std::cout << "Cubemap error\n" << faces[i] << std::endl;
            stbi_image_free(data);
        }
    }
    glTexParameteri(GL_TEXTURE_CUBE_MAP, GL_TEXTURE_MIN_FILTER, GL_LINEAR);
    glTexParameteri(GL_TEXTURE_CUBE_MAP, GL_TEXTURE_MAG_FILTER, GL_LINEAR);
    glTexParameteri(GL_TEXTURE_CUBE_MAP, GL_TEXTURE_WRAP_S, GL_CLAMP_TO_EDGE);
    glTexParameteri(GL_TEXTURE_CUBE_MAP, GL_TEXTURE_WRAP_T, GL_CLAMP_TO_EDGE);
    glTexParameteri(GL_TEXTURE_CUBE_MAP, GL_TEXTURE_WRAP_R, GL_CLAMP_TO_EDGE);

    return texture;
}

int main() {
	App->init(scr_width, scr_height);
    App->setMouseCallback(MouseCallback);
    App->setScrollCallback(ScrollCallback);

	glViewport(0, 0, scr_width, scr_height);
    glClearColor(0.1, 0.1, 0.1, 1.0);
    glEnable(GL_DEPTH_TEST);

	Shader shader("assets/shaders/scene/vertex.vert", "assets/shaders/scene/fragment.frag"), 
           skyboxshader("assets/shaders/skybox/vertex.vert", "assets/shaders/skybox/fragment.frag"),
           reflectshader("assets/shaders/reflect/vertex.vert", "assets/shaders/reflect/fragment.frag");
    
    Texture cubeTexture("assets/textures/1.jpg", 0);

    GLuint cubemapTexture = loadCubemap(faces);

    Model m_model("assets/models/bunny.obj");

    LoadVertex();

	while (App->update()) {
        float currentFrame = glfwGetTime();
        deltaTime = currentFrame - lastFrame;
        lastFrame = currentFrame;

        processInput();

        glClear(GL_COLOR_BUFFER_BIT | GL_DEPTH_BUFFER_BIT);

        reflectshader.begin();
        reflectshader.setInt("skybox", 0);
        glm::vec3 cameraPos = camera.getPostion();
        reflectshader.setVec3("cameraPos", cameraPos);
        glm::mat4 projection = glm::perspective(glm::radians(camera.getFov()), (float)scr_width / (float)scr_height, 0.1f, 100.0f);
        glm::mat4 view = camera.getViewMatrix();
        glm::mat4 model = glm::mat4(1.0f);
        model = glm::translate(model, glm::vec3(0.0));
        model = glm::scale(model, glm::vec3(1.0));
        reflectshader.setMat4("projection", projection);
        reflectshader.setMat4("view", view);
        reflectshader.setMat4("model", model);

        m_model.Draw(reflectshader);
        reflectshader.end();

        glDepthFunc(GL_LEQUAL);
        skyboxshader.begin();
        skyboxshader.setInt("skybox", 0);
        view = glm::mat4(glm::mat3(camera.getViewMatrix()));
        skyboxshader.setMat4("projection", projection);
        skyboxshader.setMat4("view", view);
        glBindVertexArray(skyboxVAO);
        glActiveTexture(GL_TEXTURE0);
        glBindTexture(GL_TEXTURE_CUBE_MAP, cubemapTexture);
        glDrawArrays(GL_TRIANGLES, 0, 36);
        glBindVertexArray(0);
        skyboxshader.end();
        glDepthFunc(GL_LESS);
	}

	App->destory();

	return 0;
}